    private void tableNameProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableNameProductMouseClicked
        // TODO add your handling code here:
        int index = tableNameProduct.getSelectedRow();
        TableModel model = tableNameProduct.getModel();
        String nameProduct = (String) model.getValueAt(index, 0);
        NewProduct product = ProductDao.getProductByName(nameProduct);
        txtProductName.setText(product.getName());
        txtProductPrice.setText(product.getPrice());
        txtProductTotal.setText(product.getPrice());
        jSpinner1.setValue(1);
        price = Integer.parseInt(product.getPrice());
        total = Integer.parseInt(product.getPrice());
        btnAddToCart.setEnabled(true);
    }//GEN-LAST:event_tableNameProductMouseClicked

