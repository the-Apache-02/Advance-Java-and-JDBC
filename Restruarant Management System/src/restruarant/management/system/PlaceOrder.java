    public void getProductNameByCategory(String category) {
        DefaultTableModel dtm = (DefaultTableModel) tableNameProduct.getModel();
        dtm.setRowCount(0);
        ArrayList<NewProduct> list = ProductDao.getAllRecordsByCategory(category);
        Iterator<NewProduct> itr = list.iterator();
        while (itr.hasNext()) {
            NewProduct product = itr.next();
            dtm.addRow(new Object[]{product.getName()});
        }
    }

    public void filterProductByName(String name, String category) {
        DefaultTableModel dtm = (DefaultTableModel) tableNameProduct.getModel();
        dtm.setRowCount(0);
        ArrayList<NewProduct> list = ProductDao.filterProductByName(name, category);
        Iterator<NewProduct> itr = list.iterator();
        while (itr.hasNext()) {
            NewProduct product = itr.next();
            dtm.addRow(new Object[]{product.getName()});
        }
    }

